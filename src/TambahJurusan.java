    private void tampilJurusanTable() {
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.setRowCount(0); // Bersihkan tabel sebelum mengisi ulang

        try {
            String query = "SELECT * FROM jurusan";
            Statement stmt = DB.conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                String kdJurusan = rs.getString("kdJurusan");
                String namaJurusan = rs.getString("namaJurusan");

                // Tambahkan baris baru ke tabel
                model.addRow(new Object[]{kdJurusan, namaJurusan});
            }
        } catch (SQLException e) {
            lbStatus.setText("Terjadi kesalahan: " + e.getMessage());
        }
    }

    

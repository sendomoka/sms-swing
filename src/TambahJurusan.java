    private void btnTambahJurusanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahJurusanActionPerformed
        // Ambil nilai dari tfkdJurusan dan tfNamaJurusan
        String kdJurusan = tfkdJurusan.getText();
        String namaJurusan = tfNamaJurusan.getText();

        // Cek apakah tfkdJurusan hanya berisi satu huruf besar
        if (kdJurusan.matches("[A-Z]{1}")) {
            try {
                // Query untuk INSERT data ke tabel jurusan
                String query = "INSERT INTO jurusan VALUES (?, ?)";
                PreparedStatement ps = DB.conn.prepareStatement(query);
                ps.setString(1, kdJurusan);
                ps.setString(2, namaJurusan);

                // Eksekusi query
                int rowsAffected = ps.executeUpdate();

                if (rowsAffected > 0) {
                    lbStatus.setText("Data berhasil ditambahkan.");
                    tfkdJurusan.setText("");
                    tfNamaJurusan.setText("");
                } else {
                    lbStatus.setText("Gagal menambahkan data.");
                }
                tampilJurusanTable();
            } catch (SQLException e) {
                lbStatus.setText("Terjadi kesalahan: " + e.getMessage());
            }
        } else {
            lbStatus.setText("Kode jurusan harus berisi satu huruf besar.");
        }
    }//GEN-LAST:event_btnTambahJurusanActionPerformed


    // Tambahkan action listener untuk tombol login
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Ambil teks yang diinputkan dari tffUsername
        String username = tfUsername.getText();
        // Ambil teks yang diinputkan dari pfPassword lalu diubah menjadi String
        String password = new String(pfPassword.getPassword());
        
        // Cek apakah username atau password kosong, lalu tampilkan alert
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "You must fill in both username and password!");
            return;
        }

        // Ambil data dari tabel otentikasi berdasarkan username dan password yang diinputkan
        String query = String.format("SELECT * FROM otentikasi WHERE username='%s' AND password='%s'", username, password);
        
        // Eksekusi query menggunakan metode DB.query() untuk mengambil ResultSet
        try {
            ResultSet resultSet = DB.query(query);
            // Cek apakah data ada di ResultSet, jika ada pindah ke tampilan CariMahasiswa, jika tidak tampilkan alert
            if (resultSet.next()) {
                new CariMahasiswa().show();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid username or password provided!");
            }
        } catch (Exception e) {
            // Tampilkan error query
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

